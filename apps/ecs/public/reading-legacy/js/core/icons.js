export function icon(name) {
  return Icons[name] || "";
}

export function moduleIconName(moduleId) {
  switch (String(moduleId || "").toLowerCase()) {
    case "reading":
      return "book";
    case "characters":
      return "users";
    case "themes":
      return "spark";
    case "timeline":
      return "clock";
    case "interactive":
    case "quiz":
    case "discussion":
      return "target";
    case "teaching":
      return "cap";
    case "map":
      return "map";
    case "philosophy":
      return "idea";
    case "precepts":
      return "quote";
    case "linguistics":
      return "flask";
    case "symbols":
      return "spark";
    case "scenes":
      return "map";
    case "suicide":
      return "quote";
    default:
      return "spark";
  }
}

export function moduleIcon(moduleId) {
  return icon(moduleIconName(moduleId));
}

